package rgcok;
class MatEntry {
    int pos, len;
    String misMatStr;
    int getPos() {
        return pos;
    }
    void setPos(int p) {
        pos = p;
    }
    int getLen() {
        return len;
    }
    void setLen(int l) {
        len = l;
    }
    String getMisMatStr() {
        return misMatStr;
    }
    void setMisMatStr(String str) {
        misMatStr = str;
    }
}
